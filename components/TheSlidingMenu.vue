<script>
import { mapGetters } from 'vuex';

import { ColorPropType } from '@/constant/prop-types';

import SearchIcon from '~/assets/icons/search.svg';
import UserBadge from '~/components/UserBadge';

const SHOW_UPLOAD_BUTTON_ROUTES = new Set(['assets-id', 'search', 'id']);

export default {
  name: 'the-sliding-menu',
  components: {
    SearchIcon,
    UserBadge,
  },
  props: {
    // eslint-disable-next-line vue/require-default-prop
    color: ColorPropType,
  },
  data() {
    return {
      isOpen: false,
    };
  },
  computed: {
    ...mapGetters(['getUserInfo']),
    buttonsColor() {
      return this.isOpen ? 'primary' : this.color;
    },
    classObject() {
      return [
        'the-sliding-menu',
        `the-sliding-menu--${this.color}`,
        `the-sliding-menu--${this.isOpen ? 'open' : 'closed'}`,
        {
          'the-sliding-menu--home': this.$route.name === 'index',
        },
      ];
    },
    currentUserId() {
      return this.getUserInfo.likecoinId;
    },
    isShowSearchButton() {
      return this.$route.name !== 'search';
    },
    isShowUploadButton() {
      return SHOW_UPLOAD_BUTTON_ROUTES.has(this.$route.name);
    },
    menuItems() {
      return [
        {
          key: 'upload',
          title: 'Upload Image Now',
          to: { name: 'upload' },
        },
        {
          key: 'my-image',
          title: 'My Images',
          to: {
            name: this.currentUserId ? 'id' : 'register',
            params: { id: this.currentUserId },
          },
        },
        {
          key: 'about',
          title: 'About puttyimages',
          to: { name: 'about' },
        },
        {
          key: 'plugins',
          title: 'Plugins and Widgets',
          to: { name: 'plugins' },
        },
        {
          key: 'support',
          title: 'Support',
          to: { name: 'support' },
        },
      ];
    },
  },
  methods: {
    onClickOutside() {
      this.isOpen = false;
    },
    onToggle() {
      this.isOpen = !this.isOpen;
    },
  },
};
</script>

