          :to="{ name: 'upload' }"
          class="the-sliding-menu__buttons__upload-button btn--likecoin hide--sm"
          color="secondary"
        >
          <v-icon class="mr-8">add_circle_outline</v-icon>
          Upload Your Image
        </v-btn>
        <v-btn
          v-else-if="isShowSearchButton"
          key="search-button"
          :color="buttonsColor"
          :to="{ name: 'search' }"
          class="btn--likecoin"
          flat
          icon
          large
        >
          <search-icon />
        </v-btn>
      </transition>
      <v-btn
        :color="buttonsColor"
        class="the-sliding-menu__buttons__toggle btn--likecoin"
        flat
        icon
        large
        @click="onToggle"
